"""
На связке Python 3.14 / ursina 8.3.0 обнаружен баг: color.rgb()/color.rgba()
не делят значения 0..255 на 255, из-за чего любой цвет с компонентом > 1
обрезается движком до белого. Эта функция всегда возвращает корректный
0..1 Vec4 вручную, независимо от поведения встроенной color.rgb().
"""

from ursina import color as _color


def rgb(r, g, b, a=255):
    return _color.rgba(r / 255, g / 255, b / 255, a / 255)
